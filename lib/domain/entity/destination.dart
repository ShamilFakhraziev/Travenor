class Destination {
  final int id;
  final List<String> photos;
  final String title;
  final String country;
  final String district;
  final double rating;
  final int price;
  final String about;

  Destination({
    required this.id,
    required this.photos,
    required this.title,
    required this.country,
    required this.district,
    required this.rating,
    required this.price,
    required this.about,
  });
}
